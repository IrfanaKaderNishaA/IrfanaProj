package operator;

public class Percentage {

	public static void main(String[] args) {
		int s1=60,s2=90,s3=80,s4=75;
		int sum=s1+s2+s3+s4;
		System.out.println(sum);
		int average=sum/4;
		System.out.println(average);
		int percentage=(average*100)/100;
		System.out.println(percentage);
				
		

	}

}
